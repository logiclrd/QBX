	public static byte[] GetFilenamesTable(CultureInfo culture)
	{
		return GetFilenamesTable(culture.TextInfo.OEMCodePage, culture.Name);
	}

	public static byte[] GetTable(int codePage, string cultureName)
	{
		string[] cultureNameParts = cultureName.ToLowerInvariant().Split('-');

		string language = cultureNameParts[0];
		string country = cultureNameParts[1];

		int slash = country.IndexOf('/');

		if (slash >= 0)
			country = country.Substring(0, slash);

		switch (codePage)
		{
			case 437:
				switch (country)
				{
					case "us":
					case "uk":
					case "fr":
					case "gr":
					case "sp":
					case "la":
					case "it":
					case "af":
					case "as":
					case "jp":
					case "ko":
					case "pr":
					case "ta":
					case "br":
						return Latin437;
					case "ca":
						return (language == "fr") ? CanadianFrench437 : Latin437;
					case "be":
					case "fi":
					case "sv":
						return Scandinavian437;
					case "ch":
						return Switzerland437;
					case "nl":
						return Netherlands437;
				}

				break;
			case 850:
				switch (country)
				{
					case "us":
					case "afe":
					case "uk":
					case "fr":
					case "it":
					case "is":
					case "as":
					case "po":
					case "ca":
						return WesternEuropean850;
					case "gr":
						return Greek850;
					case "la":
					case "sp":
					case "be":
					case "fi":
					case "sv":
					case "dk":
					case "no":
						return Scandinavian850;
					case "ch":
						return Switzerland850;
					case "nl":
						return Netherlands850;
					case "tr":
					case "ic":
					case "yu":
					case "cs":
					case "sl":
					case "hu":
					case "pl":
						return EasternEuropean850;
					case "br":
						return Brazil850;

					default:
						return WesternEuropean850;
				}
			case 852: return Slavic852;
			case 857: return Turkey857;
			case 860: return Portuguese860;
			case 861: return Iceland861;
			case 862: return Israel862;
			case 865: return Nordic865;

			case 932:
			case 934:
			case 936:
			case 938:
				return Asian;

			default:
				goto case 437;
		}

		// Fallback
		return Latin437;
	}

