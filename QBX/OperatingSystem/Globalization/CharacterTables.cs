	public static byte[] GetUppercaseTable(int codePage, string cultureName)
	{
		string[] cultureNameParts = cultureName.ToLowerInvariant().Split('-');

		string language = cultureNameParts[0];
		string country = cultureNameParts[1];

		int slash = country.IndexOf('/');

		if (slash >= 0)
			country = country.Substring(0, slash);

		switch (codePage)
		{
			case 437:
				switch (country)
				{
					case "us":
					case "uk":
					case "fr":
					case "gr":
					case "sp":
					case "la":
					case "it":
					case "af":
					case "as":
					case "jp":
					case "ko":
					case "pr":
					case "ta":
					case "br":
						return Uppercase.Latin437;
					case "ca":
						return (language == "fr") ? Uppercase.CanadianFrench437 : Uppercase.Latin437;
					case "be":
					case "fi":
					case "sv":
						return Uppercase.Scandinavian437;
					case "ch":
						return Uppercase.Switzerland437;
					case "nl":
						return Uppercase.Netherlands437;
				}

				break;
			case 850:
				switch (country)
				{
					case "us":
					case "afe":
					case "uk":
					case "fr":
					case "it":
					case "is":
					case "as":
					case "po":
					case "ca":
						return Uppercase.WesternEuropean850;
					case "gr":
						return Uppercase.Greek850;
					case "la":
					case "sp":
					case "be":
					case "fi":
					case "sv":
					case "dk":
					case "no":
						return Uppercase.Scandinavian850;
					case "ch":
						return Uppercase.Switzerland850;
					case "nl":
						return Uppercase.Netherlands850;
					case "tr":
					case "ic":
					case "yu":
					case "cs":
					case "sl":
					case "hu":
					case "pl":
						return Uppercase.EasternEuropean850;
					case "br":
						return Uppercase.Brazil850;

					default:
						return Uppercase.WesternEuropean850;
				}
			case 852: return Uppercase.Slavic852;
			case 857: return Uppercase.Turkey857;
			case 860: return Uppercase.Portuguese860;
			case 861: return Uppercase.Iceland861;
			case 862: return Uppercase.Israel862;
			case 865: return Uppercase.Nordic865;

			case 932:
			case 934:
			case 936:
			case 938:
				return Uppercase.Asian;

			default:
				goto case 437;
		}

		// Fallback
		return Uppercase.Latin437;
	}

	public static byte[] GetFilenameUppercaseTable(int codePage, string cultureName)
	{
		string[] cultureNameParts = cultureName.ToLowerInvariant().Split('-');

		string country = cultureNameParts[1];

		int slash = country.IndexOf('/');

		if (slash >= 0)
			country = country.Substring(0, slash);

		switch (codePage)
		{
			case 850:
				switch (country)
				{
					case "br":
					case "ic":
					case "tr":
					case "yu":
					case "cs":
					case "pl":
					case "hu":
					case "sl":
						return FilenameUppercase.EasternEuropean850IcelandAndBrazil;
				}

				break;
			case 852:
				return FilenameUppercase.EasternEuropean;
		}

		return FilenameUppercase.General;
	}

	public static byte[] GetCollateSequenceTable(int codePage, string cultureName)
	{
		string[] cultureNameParts = cultureName.ToLowerInvariant().Split('-');

		string language = cultureNameParts[0];
		string country = cultureNameParts[1];

		int slash = country.IndexOf('/');

		if (slash >= 0)
			country = country.Substring(0, slash);

		switch (codePage)
		{
			case 437:
				switch (country)
				{
					case "nl":
						return CollateSequence.Netherlands437;
					case "la":
					case "sp":
						return CollateSequence.Spain437;
					case "fi":
					case "sv":
						return CollateSequence.Scandinavia437;
					case "sw":
						return CollateSequence.Sweden437;
					case "be":
						return CollateSequence.Belgium437;

					default:
						return CollateSequence.General437;
				}
			case 850:
				switch (country)
				{
					case "nl":
						return CollateSequence.Netherlands850;
					case "dk":
						return CollateSequence.Denmark850;
					case "no":
						return CollateSequence.Norway850;
					case "sv":
						return CollateSequence.Sweden850;
					case "fi":
						return CollateSequence.Finland850;
					case "ch":
						return CollateSequence.Switzerland850;
					case "be":
						return CollateSequence.Belgium850;
					case "la":
					case "sp":
						return CollateSequence.Spain850;
					case "br":
						return CollateSequence.Brazil850;
					case "tr":
					case "ic":
					case "yu":
					case "cs":
					case "sl":
					case "hu":
					case "pl":
						return CollateSequence.EasternEurope850;

					default:
						return CollateSequence.General850;
				}
			case 852: return CollateSequence.EasternEurope852;
			case 860:
				switch (country)
				{
					case "pt": return CollateSequence.Portugal860;
					case "br": return CollateSequence.Brazil860;
				}

				break;
			case 861: return CollateSequence.Iceland861;
			case 862: return CollateSequence.Israel862;
			case 863: return CollateSequence.Canada863;
			case 864: return CollateSequence.AreaSouth864;
			case 865:
				switch (country)
				{
					case "dk": return CollateSequence.Denmark865;
					case "no": return CollateSequence.Norway865;
				}

				break;
			case 932: return CollateSequence.Japan932;
			case 934: return CollateSequence.Korea934;
			case 936: return CollateSequence.China936;
			case 938: return CollateSequence.Taiwan938;
		}

		return CollateSequence.General437;
	}

	public static byte[] GetDoubleByteCharacterSet(int codePage, string cultureName)
	{
		switch (codePage)
		{
			case 932: return DoubleByteCharacterSet.Japan932;
			case 934: return DoubleByteCharacterSet.Korea934;
			case 936: return DoubleByteCharacterSet.China936;
			case 938: return DoubleByteCharacterSet.Taiwan938;
		}

		return DoubleByteCharacterSet.Empty;
	}

